#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	index;
	char	*str;

	index = 0;
	if (!s)
		return (NULL);
	str = (char *) ft_calloc((ft_strlen(s)) + 1, sizeof(char));
	while (s[index])
	{
		str[index] = f(index, s[index]);
		index++;
	}
	str[index] = '\0';
	return (str);
}
